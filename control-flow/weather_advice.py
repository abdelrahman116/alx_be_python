DayCondition = input("What's the weather like today? (sunny/rainy/cold):")
if DayCondition == "sunny":
    print("Wear a t-shirt and sunglasses.")
elif DayCondition == "rainy":
    print("Don't forget to take an umbrella and a raincoat.")
elif DayCondition == "cold":
    print("Wear a warm jacket, scarf, and scarf.")
else:
    print("Sorry, I don't have advice for that weather condition.")

